using Content.Client.AI;
using Content.Client.UserInterface;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Console;

namespace Content.Client.NPC;

[GenerateTypedNameReferences]
public sealed partial class NPCWindow : FancyWindow
{
    [Dependency] private readonly IConsoleHost _console = default!;

    public NPCWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        var system = IoCManager.Resolve<IEntitySystemManager>().GetEntitySystem<ClientPathfindingDebugSystem>();

        if ((system.Modes & PathfindingDebugMode.Nodes) != 0x0)
        {
            PathNodes.Pressed = true;
        }

        if ((system.Modes & PathfindingDebugMode.Regions) != 0x0)
        {
            PathRegions.Pressed = true;
        }

        if ((system.Modes & PathfindingDebugMode.Route) != 0x0)
        {
            PathRoutes.Pressed = true;
        }

        PathNodes.OnToggled += args => _console.ExecuteCommand("pathfinder nodes");
        PathRegions.OnToggled += args => _console.ExecuteCommand("pathfinder regions");
        PathRoutes.OnToggled += args => _console.ExecuteCommand("pathfinder routes");
    }
}
