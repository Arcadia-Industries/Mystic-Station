    protected override void FrameUpdate(FrameEventArgs args)
    {
        TryToScrollToFocus();

        _updateTimer += args.DeltaSeconds;

        // Warning sign pulse        
        var lit = _gameTiming.RealTime.TotalSeconds % BlinkFrequency > BlinkFrequency / 2f;
        SystemWarningPanel.Modulate = lit ? Color.White : new Color(178, 178, 178);

        if (_updateTimer >= UpdateTime)
        {
            _updateTimer -= UpdateTime;

            // Request update from power monitoring system
            RequestPowerMonitoringUpdateAction?.Invoke(_entManager.GetNetEntity(_trackedEntity), GetCurrentPowerMonitoringConsoleGroup());
        }
    }

    private bool TryGetEntityNavMapTrackingData(EntityUid uid, [NotNullWhen(true)] out NavMapTrackingData? data)
    {
        data = null;

        if (_navMapTracking == null)
            return false;

        data = _navMapTracking.TrackingData.FirstOrDefault(x => _entManager.GetEntity(x.NetEntity) == uid);

        return data != null;
    }
