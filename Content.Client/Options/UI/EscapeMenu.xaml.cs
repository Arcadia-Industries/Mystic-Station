using Robust.Client.AutoGenerated;
using Robust.Client.Console;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Options.UI
{
    [GenerateTypedNameReferences]
    public sealed partial class EscapeMenu : DefaultWindow
    {
        public EscapeMenu(IClientConsoleHost consoleHost)
        {
            RobustXamlLoader.Load(this);
            //lambdas are sexy :P
            OptionsButton.OnPressed += (args) =>
            {
                new OptionsMenu().OpenCentered();
                Dispose();
            };
            QuitButton.OnPressed += (args) => {
                consoleHost.ExecuteCommand("quit");
                Dispose();

            };
            AHelpButton.OnPressed += (args) =>
            {
                consoleHost.ExecuteCommand("openahelp");
                Dispose();
            };
            DisconnectButton.OnPressed += (args) => {
                consoleHost.ExecuteCommand("disconnect");
                Dispose();

            };
        }
    }
}
