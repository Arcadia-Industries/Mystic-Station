
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Speech.UI
{
    [GenerateTypedNameReferences]
    public sealed partial class VoiceChangerWindow : DefaultWindow
    {
        public event Action<string>? OnNameEntered;
        public VoiceChangerWindow()
        {
            RobustXamlLoader.Load(this);

            NameLineEdit.OnTextEntered += e => OnNameEntered?.Invoke(e.Text);
        }

        public void SetCurrentName(string name)
        {
            NameLineEdit.Text = name;
        }
    }
}
