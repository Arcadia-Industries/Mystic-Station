namespace Content.Client.Guidebook.Controls;

/// <summary>
///     Control for embedding a microwave recipe into a guidebook.
/// </summary>
[UsedImplicitly, GenerateTypedNameReferences]
public sealed partial class GuideMicrowaveRecipeEmbed : BoxContainer, IDocumentTag, ISearchableControl
{
    [Dependency] private readonly IEntitySystemManager _systemManager = default!;
    [Dependency] private readonly IPrototypeManager _prototype = default!;
    [Dependency] private readonly IResourceCache _resources = default!;

    public GuideMicrowaveRecipeEmbed()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        MouseFilter = MouseFilterMode.Stop;
    }

    public GuideMicrowaveRecipeEmbed(string recipe) : this()
    {
        GenerateControl(_prototype.Index<FoodRecipePrototype>(recipe));
    }

    public GuideMicrowaveRecipeEmbed(FoodRecipePrototype recipe) : this()
    {
        GenerateControl(recipe);
    }

    public bool CheckMatchesSearch(string query)
    {
        return this.ChildrenContainText(query);
    }

    public void SetHiddenState(bool state, string query)
    {
        this.Visible = CheckMatchesSearch(query) ? state : !state;
    }

    public bool TryParseTag(Dictionary<string, string> args, [NotNullWhen(true)] out Control? control)
    {
        control = null;
        if (!args.TryGetValue("Recipe", out var id))
        {
            Logger.Error("Recipe embed tag is missing recipe prototype argument");
            return false;
        }

        if (!_prototype.TryIndex<FoodRecipePrototype>(id, out var recipe))
        {
            Logger.Error($"Specified recipe prototype \"{id}\" is not a valid recipe prototype");
            return false;
        }

        GenerateControl(recipe);

        control = this;
        return true;
    }

    private void GenerateControl(FoodRecipePrototype recipePrototype)
    {
        var reactantsCount = recipePrototype.IngredientsReagents.Count + recipePrototype.IngredientsSolids.Count;

        foreach (var (ingredient, amount) in recipePrototype.IngredientsReagents)
        {
            var ingredientBox = new BoxContainer() { Align = AlignMode.Center };
            var reagentPrototype = _prototype.Index<ReagentPrototype>(ingredient);

            ingredientBox.AddChild(new TextureRect() { TexturePath = "/Textures/Interface/Misc/reagent.png" });

            var ingredientName = new RichTextLabel();
            var ingredientNameMsg = new FormattedMessage();
            ingredientNameMsg.AddMarkup(Loc.GetString("guidebook-food-microwave-reagent-display",
                ("name", reagentPrototype.LocalizedName),
                ("amount", amount)));
            ingredientName.SetMessage(ingredientNameMsg);
            ingredientBox.AddChild(ingredientName);

            IngredientsLabel.AddChild(ingredientBox);
        }

        foreach (var (ingredient, amount) in recipePrototype.IngredientsSolids)
        {
            var ingredientBox = new BoxContainer() { Align = AlignMode.Center };
            var entityPrototype = _prototype.Index<EntityPrototype>(ingredient);

            ingredientBox.AddChild(new LayeredTextureRect { Textures = SpriteComponent.GetPrototypeTextures(entityPrototype, _resources).Select(o => o.Default).ToList() });

            var ingredientName = new RichTextLabel();
            var ingredientNameMsg = new FormattedMessage();
            ingredientNameMsg.AddMarkup(Loc.GetString("guidebook-food-microwave-entity-display",
                ("name", _prototype.Index<EntityPrototype>(ingredient).Name),
                ("amount", amount)));
            ingredientName.SetMessage(ingredientNameMsg);
            ingredientBox.AddChild(ingredientName);

            IngredientsLabel.AddChild(ingredientBox);
        }

        TimeLabel.Text = Loc.GetString("guidebook-food-microwave-time",
            ("time", recipePrototype.CookTime));

        var productPrototype = _prototype.Index<EntityPrototype>(recipePrototype.Result);
        ProductIcon.Textures = SpriteComponent.GetPrototypeTextures(productPrototype, _resources).Select(o => o.Default).ToList();
        var productMsg = new FormattedMessage();
        productMsg.AddMarkup(Loc.GetString("guidebook-food-microwave-entity-display",
            ("name", productPrototype.Name),
            ("amount", 1)));
        productMsg.Pop();
        ProductLabel.SetMessage(productMsg);
    }
}
