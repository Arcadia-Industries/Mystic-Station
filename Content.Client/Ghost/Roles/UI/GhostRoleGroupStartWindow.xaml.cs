using System.ComponentModel;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Ghost.Roles.UI;

[GenerateTypedNameReferences]
public sealed partial class GhostRoleGroupStartWindow : DefaultWindow
{
    public delegate void StartRoleGroup(string name, string description, string rules);

    public GhostRoleGroupStartWindow(StartRoleGroup startAction)
    {
        RobustXamlLoader.Load(this);

        StartButton.OnPressed += _ => startAction.Invoke(RoleName.Text, RoleDescription.Text, RoleRules.Text);
    }
}
