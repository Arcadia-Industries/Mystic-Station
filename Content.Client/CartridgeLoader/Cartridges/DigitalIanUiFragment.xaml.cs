using Content.Shared.CartridgeLoader.Cartridges;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.GameObjects;
using Robust.Shared.Utility;

namespace Content.Client.CartridgeLoader.Cartridges;

[GenerateTypedNameReferences]
public sealed partial class DigitalIanUiFragment : BoxContainer
{
    public event Action? OnFeedButtonPressed;
    public event Action? OnPetButtonPressed;

    public DigitalIanUiFragment()
    {
        RobustXamlLoader.Load(this);
        FeedButton.OnPressed += _ => OnFeedButtonPressed?.Invoke();
        PetButton.OnPressed += _ => OnPetButtonPressed?.Invoke();

        Icon.SetFromSpriteSpecifier(new SpriteSpecifier.Rsi(new("Mobs/Pets/corgi.rsi"), "ian"));
        Icon.DisplayRect.TextureScale = (6, 6);
    }
}