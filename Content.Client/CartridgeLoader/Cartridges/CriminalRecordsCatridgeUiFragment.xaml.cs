using Content.Shared.CriminalRecords;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.CartridgeLoader.Cartridges;

[GenerateTypedNameReferences]
public sealed partial class CriminalRecordsCatridgeUiFragment : BoxContainer
{
    public CriminalRecordsCatridgeUiFragment()
    {
        RobustXamlLoader.Load(this);

        Orientation = LayoutOrientation.Vertical;
        HorizontalExpand = true;
        VerticalExpand = true;
    }

    public void UpdateState(IEnumerable<(uint, CriminalRecord)>? records)
    {
        CriminalRecordsListing.DisposeAllChildren();
        CriminalRecordsListing.RemoveAllChildren();


        if (records == null)
            return;

        CriminalRecordsListing.AddCriminalRecords(records);
    }
}
