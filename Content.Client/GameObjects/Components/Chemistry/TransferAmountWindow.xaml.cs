using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.GameObjects.Components.Chemistry
{
    [GenerateTypedNameReferences]
    public partial class TransferAmountWindow : SS14Window
    {
        public Slider amountSlider => AmountSlider;
        public Button applyButton => ApplyButton;
        public LineEdit amountLineEdit => AmountLineEdit;
        public TransferAmountWindow(int minValue, int maxValue)
        {
            RobustXamlLoader.Load(this);
            AmountSlider.Value = maxValue + minValue / 2;
            AmountSlider.MinValue = minValue;
            AmountSlider.MaxValue = maxValue;
        }
    }
}
