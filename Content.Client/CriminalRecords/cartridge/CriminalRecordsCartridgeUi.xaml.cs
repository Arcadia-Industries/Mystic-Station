using Content.Shared.CriminalRecords;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.CriminalRecords;

[GenerateTypedNameReferences]
public sealed partial class CriminalRecordsCartridgeUi : DefaultWindow
{
    public CriminalRecordsCartridgeUi()
    {
        RobustXamlLoader.Load(this);

        StationName.AddStyleClass("LabelBig");
    }

    public void Populate(string name, IEnumerable<(uint, CriminalRecord)>? records)
    {
        CriminalRecordsListing.DisposeAllChildren();
        CriminalRecordsListing.RemoveAllChildren();

        StationNameContainer.Visible = records != null;
        StationName.Text = name;

        if (records == null)
            return;

        CriminalRecordsListing.AddCriminalRecords(records);
    }
}
