using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.UserInterface.Controls
{
    [GenerateTypedNameReferences]
    public partial class SplitBar : BoxContainer
    {
        public SplitBar()
        {
            RobustXamlLoader.Load(this);
        }

        public void Clear()
        {
            DisposeAllChildren();
        }

        public void AddEntry(float amount, Color color, string? tooltip = null)
        {
            var left = ChildCount == 0 ? 0f : 2f;

            // Fix left one's margin
            if (ChildCount > 0)
            {
                var leftControl = (PanelContainer) GetChild(ChildCount - 1);

                if (leftControl.PanelOverride != null)
                    leftControl.PanelOverride.PaddingRight = 2f;
            }

            AddChild(new PanelContainer
            {
                ToolTip = tooltip,
                HorizontalExpand = true,
                SizeFlagsStretchRatio = amount,
                MouseFilter = MouseFilterMode.Stop,
                PanelOverride = new StyleBoxFlat
                {
                    BackgroundColor = color,
                    PaddingLeft = left,
                    PaddingRight = 2f,
                },
                MinSize = new Vector2(24, 0)
            });
        }
    }
}
