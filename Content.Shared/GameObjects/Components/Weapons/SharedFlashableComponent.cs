using System;
using Robust.Shared.AutoGenerated;
using Robust.Shared.GameObjects;
using Robust.Shared.Network;
using Robust.Shared.Serialization;

namespace Content.Shared.GameObjects.Components.Weapons
{
    [NetID("FLASHABLE")]
    public class SharedFlashableComponent : Component
    {
        public override string Name => "Flashable";
        public override uint? NetID => NetIDs.FLASHABLE;
    }

    [Serializable, NetSerializable]
    public class FlashComponentState : ComponentState
    {
        public double Duration { get; }
        public TimeSpan Time { get; }

        public FlashComponentState(double duration, TimeSpan time) : base(NetIDs.FLASHABLE)
        {
            Duration = duration;
            Time = time;
        }
    }
}
